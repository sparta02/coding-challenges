SELECT A.AUTHOR_ID, B.AUTHOR_NAME, A.CATEGORY, SUM(C.TOTAL*A.PRICE) AS SALES
FROM BOOK A JOIN AUTHOR B JOIN TOTAL_SALES C
ON A.AUTHOR_ID=B.AUTHOR_ID AND A.BOOK_ID=C.BOOK_ID
GROUP BY A.AUTHOR_ID, A.CATEGORY
ORDER BY A.AUTHOR_ID ASC, A.CATEGORY DESC