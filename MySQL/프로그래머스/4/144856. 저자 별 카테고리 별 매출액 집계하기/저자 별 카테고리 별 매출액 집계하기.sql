WITH TOTAL_SALES AS (
    SELECT BOOK_ID,SUM(SALES) TOTAL
    FROM BOOK_SALES
    WHERE SALES_DATE BETWEEN '2022-01-01' AND '2022-01-31'
    GROUP BY BOOK_ID)

